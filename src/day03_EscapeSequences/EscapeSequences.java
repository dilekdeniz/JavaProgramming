package day03_EscapeSequences;
/*
\n: starts a new line
\t: tab

 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC");
        System.out.println("-------------");

        System.out.println("Hello Cydeo\n How are you all today? \n nice to see you all. What class will be have next week?");
        System.out.println("------------------------");
        System.out.println("\t Java is Cool Programming Language");




    }
}
